document.getElementById("diagnosisForm").addEventListener("submit", function(e) {
    e.preventDefault();
    const end_time = Date.now();
    const duration = ((end_time - start_time) / 1000).toFixed(2); // seconds

    const form_data = new FormData(this);
    let user_atbildes = []
    for (const [key, val] of form_data) {
        user_atbildes.push(val);
    }
    let atzimetas_sk = user_atbildes.length;
    let pareizas_sk = 0;
    let nepareizas_sk = 0;
    let neatzimetas_sk = 0;
    let viss_saraksts = curr_test_list[img_index]["Atbildes"];
    let pareizas_atbildes = Object.keys(viss_saraksts).filter(k => viss_saraksts[k] == 1);
    for (let atb of user_atbildes) {
        if (pareizas_atbildes.includes(atb)) {
            pareizas_sk++;
        }
    }
    nepareizas_sk = atzimetas_sk - pareizas_sk; // var velak aprekinat tbh
    neatzimetas_sk = pareizas_atbildes.length - pareizas_sk;

    // send/publish result
    const url = script_url
        + "?run_id="         + encodeURIComponent(run_id)
        + "&username="       + encodeURIComponent(username)
        + "&version="        + encodeURIComponent(version)
        + "&img="            + encodeURIComponent(curr_test_list[img_index]["Bilde"])
        + "&atzimetas_sk="   + encodeURIComponent(atzimetas_sk)
        + "&pareizas_sk="    + encodeURIComponent(pareizas_sk)
        + "&nepareizas_sk="  + encodeURIComponent(nepareizas_sk)
        + "&neatzimetas_sk=" + encodeURIComponent(neatzimetas_sk)
        + "&time_spent="     + encodeURIComponent(duration);
    fetch(url)
    .then(response => response.text())
    .then(result => {
        // alert("Thank you! Your response has been saved.");
        console.log("Server result:", result, "Run ID:", run_id);
    })
    .catch(error => {
        alert("Radās kļūda iesniedzot iepriekšējo atbildi.");
        console.error("Error:", error);
    });

    // go to next image
    img_index++;
    if (img_index >= curr_test_list.length) {
        // no more images
        // show thank you page
        diagnosis_input_page.style.display = "none";
        thank_you_page.style.display = "block";
        return;
    }
    document.getElementById("img").src = "";
    update_test_view();
    start_time = Date.now();
});

